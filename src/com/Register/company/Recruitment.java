package com.Register.company;

public class Recruitment {
	
	private int cp_id;
	private String cp_name;
	private int cp_sal;
	private String cp_profile;
	private String cp_pass;
	
	
	
	public String getCp_pass() {
		return cp_pass;
	}
	public void setCp_pass(String cp_pass) {
		this.cp_pass = cp_pass;
	}
	
	public String getCp_profile() {
		return cp_profile;
	}
	public void setCp_profile(String cp_profile) {
		this.cp_profile = cp_profile;
	}
	public int getCp_Id() {
		return cp_id;
	}
	public void setCp_id(int cp_id) {
		this.cp_id = cp_id;
	}
	public String getCp_name() {
		return cp_name;
	}
	public void setCp_name(String cp_name) {
		this.cp_name = cp_name;
	}
	public int getCp_sal() {
		return cp_sal;
	}
	public void setCp_sal(int cp_sal) {
		this.cp_sal = cp_sal;
	}
	
	
	

}
